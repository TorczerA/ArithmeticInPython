#Arithmetics in Python

a = 100
b = 200

print("a = ",a)
print("b = ",b)
print("a+b =",a+b)
print("a-b =",a-b)
print("a*b =",a*b)
print("a/b =",a/b)
print("a%b =",a%b)

c = a + b

print("c = a + b =",c)

print("a = a + b -> a += b")

print("a = a - b -> a -= b")

print("a = a * b -> a *= b")

print("a = a / b -> a /= b")